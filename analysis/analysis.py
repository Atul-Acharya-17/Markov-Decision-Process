"""# Impot libraries"""

import numpy as np
import pandas as pd
import plotly.graph_objects as go

"""---

# Analysis of Value Iteration
"""

VI_data = pd.read_csv('value_iteration.csv')

VI_data.head()

VI_data.tail()

VI_data.shape

"""## Interative plot of utilities of every state vs iterations"""

x = np.linspace(1, VI_data.shape[0], VI_data.shape[0])

fig = go.Figure()

for state in VI_data:
  fig.add_trace(go.Scatter(x=x, y=VI_data[state],
                    mode='lines',
                    name=state))
  
fig.update_layout(legend=dict(
    orientation="h",
    yanchor="bottom",
    y=1.02,
    xanchor="right",
    x=1
))

fig.show()

"""## Static plot of utilities of every state vs iterations"""

x = np.linspace(1, VI_data.shape[0], VI_data.shape[0])

fig = go.Figure()

for state in VI_data:
  fig.add_trace(go.Scatter(x=x, y=VI_data[state],
                    mode='lines',
                    name=state))
  
fig.update_layout(
    legend=dict(
    orientation="h",
    yanchor="bottom",
    y=1.02,
    xanchor="right",
    x=1,
))
fig.show("png")
fig.write_image("value_iteration_plot.png")

